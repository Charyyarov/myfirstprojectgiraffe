public class giraffe_drawing_a_triangle {
    public static void main(String [] args) {
        System.out.println("   /|");
        System.out.println("  / |");
        System.out.println(" /  |");
        System.out.println("/___|");
    }
}

/* As we can see, Java executes the codes line by line,
as if you gave a receipt to cook  food to someone, and
they follow the receipt step by step. So Java will execute the
first code as you can see in the console, then the next line, next and next.
So if you run the program, it will print out a beautiful triangle.
 */